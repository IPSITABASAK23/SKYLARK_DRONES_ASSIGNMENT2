import torch
from torch.utils.data import Dataset

class GCPDataset(Dataset):

    def __init__(self, labels, dataset_path, patch_size=224):
        # Filter keys to only include those with 'verified_shape'
        self.keys = [k for k in labels.keys() if "verified_shape" in labels[k]]
        self.labels = labels
        self.dataset_path = dataset_path
        self.patch_size = patch_size

    def __len__(self):
        return len(self.keys)

    def __getitem__(self, idx):

        k = self.keys[idx]

        path = os.path.join(self.dataset_path, k)

        img = cv2.imread(path)

        x = self.labels[k]["mark"]["x"]
        y = self.labels[k]["mark"]["y"]

        patch = extract_patch(img, x, y, self.patch_size)

        patch = cv2.cvtColor(patch, cv2.COLOR_BGR2RGB)

        patch = torch.tensor(patch).permute(2,0,1).float()/255.0

        shape = self.labels[k]["verified_shape"]
        label = shape_to_id[shape]

        return patch, label

