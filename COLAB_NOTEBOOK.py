def extract_patch(img, x, y, patch_size=224):

    half = patch_size // 2

    h, w = img.shape[:2]

    x = int(x)
    y = int(y)

    x1 = max(0, x - half)
    x2 = min(w, x + half)
    y1 = max(0, y - half)
    y2 = min(h, y + half)

    patch = img[y1:y2, x1:x2]

    patch = cv2.resize(patch, (patch_size, patch_size))

    return patch

